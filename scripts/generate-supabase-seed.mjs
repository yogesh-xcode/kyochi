import fs from 'node:fs';
import path from 'node:path';

const root = process.cwd();
const dataDir = path.join(root, 'src', 'data');
const outFile = path.join(root, 'supabase', 'seed.sql');

const readJson = (name) =>
  JSON.parse(fs.readFileSync(path.join(dataDir, `${name}.json`), 'utf8'));

const escapeSqlString = (value) => String(value).replaceAll("'", "''");
const sqlValue = (value) => {
  if (value === null || value === undefined) return 'NULL';
  if (typeof value === 'number') return Number.isFinite(value) ? String(value) : 'NULL';
  if (typeof value === 'boolean') return value ? 'true' : 'false';
  return `'${escapeSqlString(value)}'`;
};

const insertRows = (table, columns, rows) => {
  if (!rows.length) return `-- ${table}: no rows\n`;
  const values = rows
    .map((row) => `(${columns.map((col) => sqlValue(row[col])).join(', ')})`)
    .join(',\n');
  return `insert into public.${table} (${columns.join(', ')}) values\n${values}\non conflict (id) do update set\n${columns
    .filter((c) => c !== 'id')
    .map((c) => `${c} = excluded.${c}`)
    .join(',\n')};\n`;
};

const franchises = readJson('franchises');
const therapies = readJson('therapies');
const patients = readJson('patients');
const therapists = readJson('therapists');
const appointments = readJson('appointments');
const billing = readJson('billing');
const feedback = readJson('feedback').map((row) => ({
  ...row,
  invoice_id: row.invoice_id === 'N/A' ? null : row.invoice_id,
}));
const users = readJson('users');
const notifications = readJson('notifications');

const sql = [
  '-- generated by scripts/generate-supabase-seed.mjs',
  'begin;',
  'truncate table public.feedback restart identity cascade;',
  'truncate table public.billing restart identity cascade;',
  'truncate table public.appointments restart identity cascade;',
  'truncate table public.therapists restart identity cascade;',
  'truncate table public.patients restart identity cascade;',
  'truncate table public.therapies restart identity cascade;',
  'truncate table public.franchises restart identity cascade;',
  'truncate table public.app_users restart identity cascade;',
  'truncate table public.notifications restart identity cascade;',
  'truncate table public.access_requests restart identity cascade;',
  '',
  insertRows('franchises', ['id', 'name', 'city', 'region', 'address', 'phone', 'whatsapp'], franchises),
  insertRows('therapies', ['id', 'name', 'category', 'duration_min', 'session_count', 'status', 'description'], therapies),
  insertRows('patients', ['id', 'franchise_id', 'full_name', 'email', 'phone', 'dob', 'wellness_score', 'status'], patients),
  insertRows('therapists', ['id', 'franchise_id', 'full_name', 'email', 'specialty', 'license_no', 'status'], therapists),
  insertRows('appointments', ['id', 'franchise_id', 'patient_id', 'therapist_id', 'therapy_id', 'starts_at', 'status'], appointments),
  insertRows('billing', ['id', 'appointment_id', 'patient_id', 'amount', 'currency', 'due_date', 'status'], billing),
  insertRows('feedback', ['id', 'appointment_id', 'patient_id', 'therapist_id', 'invoice_id', 'session_date', 'rating', 'status', 'notes'], feedback),
  insertRows('app_users', ['id', 'full_name', 'email', 'role', 'status', 'franchise_id', 'therapist_id'], users),
  insertRows('notifications', ['id', 'title', 'message', 'time', 'is_read'], notifications),
  'commit;',
  '',
].join('\n');

fs.writeFileSync(outFile, sql);
console.log(`Generated ${path.relative(root, outFile)}`);
